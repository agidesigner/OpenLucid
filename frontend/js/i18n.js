// ── Page namespace auto-detection ───────────────────────────────────────────

(function() {
  const path = location.pathname.replace(/^\//, '').replace(/\.html$/, '') || 'index';
  // Map URL path to I18N namespace
  const nsMap = {
    'index': 'index',
    'install': 'install',
    'signin': 'signin',
    'offer': 'offer',
    'apps': 'apps',
    'setting': 'setting',
    'account': 'account',
    'topic-studio': 'topic_studio',
    'kb-qa': 'kb_qa',
    'script-writer': 'script_writer',
    'content-studio': 'script_writer',  // reuse same namespace (same composer keys)
    'strategy-unit': 'strategy_unit',
    'brandkit': 'brandkit',
    'brandkit-list': 'brandkit_list',
    'creations': 'creations',
    'video-studio': 'video_studio',
  };
  window._odPageNs = nsMap[path] || null;
})();


// ── Global t() function ─────────────────────────────────────────────────────

window.t = function(key) {
  const lang = window._odLang;
  // 1. Try page namespace
  if (window._odPageNs) {
    const pageDict = window.I18N[window._odPageNs];
    if (pageDict && pageDict[lang] && pageDict[lang][key] !== undefined) {
      return pageDict[lang][key];
    }
  }
  // 2. Try common namespace
  const commonDict = window.I18N.common;
  if (commonDict && commonDict[lang] && commonDict[lang][key] !== undefined) {
    return commonDict[lang][key];
  }
  // 3. Fallback: return key itself
  return key;
};


// ── Clipboard helper ────────────────────────────────────────────────────────
//
// `navigator.clipboard.writeText` is only available in secure contexts
// (HTTPS or localhost). OpenLucid is commonly self-hosted on plain HTTP
// (LAN, intranet, behind a non-TLS reverse proxy), so we MUST have a
// fallback or every copy button breaks.
//
// Modern API path → HTTPS / localhost
// Legacy execCommand path → plain HTTP (deprecated but no browser has
// removed it because there is no other way to copy on insecure contexts)
//
// Returns Promise<bool> so callers can show a "Copied!" / "Failed" state.

window.odCopyText = async function(text) {
  if (navigator.clipboard && navigator.clipboard.writeText) {
    try {
      await navigator.clipboard.writeText(text);
      return true;
    } catch (e) {
      // Fall through to legacy fallback (permission denied, etc.)
    }
  }
  try {
    const ta = document.createElement('textarea');
    ta.value = text;
    ta.setAttribute('readonly', '');
    ta.style.position = 'fixed';
    ta.style.top = '-9999px';
    ta.style.left = '-9999px';
    ta.style.opacity = '0';
    document.body.appendChild(ta);
    ta.select();
    const ok = document.execCommand('copy');
    document.body.removeChild(ta);
    return ok;
  } catch (e) {
    console.error('odCopyText fallback failed:', e);
    return false;
  }
};


